# 숫자형
a = 10  # 정수형
b = 2.0 # 실수형
# + , - , * , / , //(몫) , % (나머지) , **
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(10//3) # 3
print(10%3)  # 1
print(10+3*2) # 16
print(2**4)   # 2*2*2*2 = 16
